task usercontrol()
{
	startTask(manageMusic);														//Plays randomly-chosen music in the last seconds of a match
	startTask(displayBattery);												//Display the battery voltage
	startTask(drive);

	int liftTarget = potToThousandths(LIFT);					//The lift target starts wherever the lift is
	int clawRotTarget = potToThousandths(CLAW_ROT);		//The claw rotation starts wherever the claw rotation value is

	int* stackCountL = &stackCountG;									//Creates a local variable for the cones being stacked

	int clawState = false;
	int upPressed = false;
	int clawRotState = false;
	int downPressed = false;

	clearTimer(T1);																		//Clears the timer so music can be played at the end of the match

	while (1)
	{
		//Ch2					=		Right drive
		//Ch3					=		Left drive
		//5U and 5D		=		Lift
		//6U and 6D		=		Cone claw toggle
		//6D					=		Cone claw toggle rotation
		//7L					=		Mobile goal up
		//7D					=		Mobile goal down
		//8D					=		Stack cone
		//8R					=		Reset count

		//Lift input:
		if(vexRT[Btn5U] || vexRT[Btn5D])									//5U and 5D shoulder buttons increment and decrement the lift target
			liftTarget -= (vexRT[Btn5U] - vexRT[Btn5D]);

		pTuneLift(&liftTarget);														//P-tunes the lift

		//Claw input:
		if(vexRT[Btn6U] && !(upPressed)) {								//6U toggles the claw
			clawState = !clawState;
			upPressed = true;
		}
		if(!(vexRT[Btn6U]))
			upPressed = false;

		if (clawState)
			pTuneClaw(1000);
		else if (!clawState)
			pTuneClaw(0);

		//Claw rotation input:
		if(vexRT[Btn6D] && !(downPressed)) {							//6D toggles the claw rotation
			clawRotState = !clawRotState;
			downPressed = true;
		}
		if(!(vexRT[Btn6D]))
			downPressed = false;

		if(clawRotState)
			pTuneClawRot(1000);
		if(!clawRotState)
			pTuneClawRot(0);

		//Mobile goal lift input:
		if(vexRT[Btn7L])																	//Raises the lift if 7L is pressed
			pTuneMGLift(1000);
		else if(vexRT[Btn7D])															//Lowers the lift if 7D is pressed
			pTuneMGLift(0);

		/* Stack function and stack reset
		*  See void stackCone(int* targets) */
		if(vexRT[Btn8D]) {
			*stackCountL = *stackCountL++;
			int currentStackHeight = *stackCountL * STACK_HEIGHT;

			testLift(currentStackHeight + STACK_HEIGHT);
			testClawRot(1000);
			testLift(currentStackHeight);
			testClaw(1000);
			testLift(currentStackHeight + 2 * STACK_HEIGHT);
			testClawRot(0);
			testLift(0);

			playTone(1050, 50);
		}

		else if(vexRT[Btn8R]) {														//Resets the stack count if 8L is pressed
			*stackCountL = 0;
			playTone(525, 50);
		}
	}
}
