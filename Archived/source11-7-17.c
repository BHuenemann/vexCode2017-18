/* Function for auto stacking a cone */
void stackCone(int* targets) {

	int* stackCountL = &stackCountG;												//Setup a local variable and increment it by one
	*stackCountL++;
	int currentStackHeight = *stackCountL * STACK_HEIGHT;		//Create a variable for the current height of the cone stack

	setClaw(targets, CONE_CLAW_GRAB);												//Grabs the cone
	setLift(targets, currentStackHeight + STACK_HEIGHT);		//Raises the lift to the right height
	setClawRot(targets, 1000);															//Rotates the claw downwards
	setLift(targets, currentStackHeight);										//Lowers the cone onto the mobile goal
	setClaw(targets, 1000);																	//Releases the cone
	setLift(targets, currentStackHeight + 2 * STACK_HEIGHT);//Raises the lift again
	setClawRot(targets, 0);																	//Rotates the claw forwards
	setLift(targets, 0);																		//Lowers the lift
	setClaw(targets, 1000);																	//Opens the claw

	playTone(1050, 50);																			//Plays a tone when finished
}

/* Eliminates motor whine on the drive during user control */
int axesFix(int remote) {
	if(abs(remote) >= THRESHOLD) 		//Returns 0 if the value does not pass the TRESHOLD constant
		return remote;
	else
		return 0;
}



